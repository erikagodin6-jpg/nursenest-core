function main() {
  const enPath = path.join(__dirname, '../client/public/i18n/en.json');
  const en = JSON.parse(fs.readFileSync(enPath, 'utf8'));

  const enWithClarity = { ...en, ...CLARITY_EN };
  const enSorted = {};
  Object.keys(enWithClarity).sort().forEach(k => { enSorted[k] = enWithClarity[k]; });
  fs.writeFileSync(enPath, JSON.stringify(enSorted));
  console.log(`en: added ${Object.keys(CLARITY_EN).length} clarity keys, total: ${Object.keys(enSorted).length}`);

  const enKeys = Object.keys(enSorted);
  const allLangs = ["fr","tl","hi","es","zh","zh-tw","ar","ko","pt","pa","vi","ht","ur","ja","fa","de","th","tr","id"];
  const report = {};

  for (const lang of allLangs) {
    const jsonPath = path.join(__dirname, `../client/public/i18n/${lang}.json`);
    const existing = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
    const missing = enKeys.filter(k => !existing[k]);

    if (missing.length === 0) {
      console.log(`${lang}: 0 missing, skipping`);
      report[lang] = { added: 0, remaining: 0 };
      continue;
    }

    const shortTranslations = SHORT_KEYS[lang] || {};
    const faqTranslations = buildFaqTranslations(lang);
    const allNew = { ...shortTranslations, ...faqTranslations };

    const newEntries = {};
    for (const k of missing) {
      if (allNew[k]) {
        newEntries[k] = allNew[k];
      }
    }

    const merged = { ...existing, ...newEntries };
    const sorted = {};
    Object.keys(merged).sort().forEach(k => { sorted[k] = merged[k]; });
    fs.writeFileSync(jsonPath, JSON.stringify(sorted));

    const stillMissing = enKeys.filter(k => !sorted[k]).length;
    report[lang] = { added: Object.keys(newEntries).length, remaining: stillMissing, totalKeys: Object.keys(sorted).length };
    console.log(`${lang}: added ${Object.keys(newEntries).length}, remaining missing: ${stillMissing}, total: ${Object.keys(sorted).length}`);
  }

  console.log('\n=== REPORT ===');
  console.log(JSON.stringify(report, null, 2));
}

main();
