import * as fs from 'fs';
import * as path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const EN_FILE = path.join(__dirname, '../tools/i18n/source/i18n-en.ts');
const enContent = fs.readFileSync(EN_FILE, 'utf-8');

const enEntries: Record<string, string> = {};
const regex = /"(newGrad\.[^"]+)":\s*"([^"]*)"/g;
let m;
while ((m = regex.exec(enContent)) !== null) {
  enEntries[m[1]] = m[2];
}

console.log(`Found ${Object.keys(enEntries).length} English newGrad keys`);

type TMap = Record<string, string>;

function injectTranslations(lang: string, translations: TMap): void {
  const filePath = path.join(__dirname, `../tools/i18n/source/i18n-${lang}.ts`);
  let content = fs.readFileSync(filePath, 'utf-8');

  const existingCount = (content.match(/"newGrad\./g) || []).length;
  if (existingCount > 10) {
    console.log(`${lang}: Already has ${existingCount} newGrad keys, skipping`);
    return;
  }

  const entries = Object.entries(translations)
    .map(([key, val]) => `    "${key}": "${val.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}",`)
    .join('\n');

  const insertionPoint = content.lastIndexOf('};');
  if (insertionPoint === -1) {
    console.error(`${lang}: Could not find closing }; in file`);
    return;
  }

  content = content.slice(0, insertionPoint) + '\n\n    // New Grad Career Hub\n' + entries + '\n' + content.slice(insertionPoint);

  fs.writeFileSync(filePath, content, 'utf-8');
  console.log(`${lang}: Added ${Object.keys(translations).length} newGrad translations`);
}

