function main() {
  const enPath = path.join(__dirname, '../client/public/i18n/en.json');
  const en = JSON.parse(fs.readFileSync(enPath, 'utf8'));
  const enKeys = Object.keys(en);

  const tlPath = path.join(__dirname, '../client/public/i18n/tl.json');
  const tl = JSON.parse(fs.readFileSync(tlPath, 'utf8'));

  const missing = enKeys.filter(k => !tl[k]);
  console.log('Filipino missing before: ' + missing.length);

  let added = 0;
  for (const k of missing) {
    if (tlTranslations[k]) {
      tl[k] = tlTranslations[k];
      added++;
    }
  }

  const remainingMissing = enKeys.filter(k => !tl[k]);

  if (remainingMissing.length > 0) {
    console.log('Still missing ' + remainingMissing.length + ' keys, using English fallback:');
    for (const k of remainingMissing) {
      tl[k] = en[k];
      added++;
    }
  }

  const sorted = {};
  Object.keys(tl).sort().forEach(k => { sorted[k] = tl[k]; });
  fs.writeFileSync(tlPath, JSON.stringify(sorted));

  const finalMissing = enKeys.filter(k => !sorted[k]);
  console.log('Added: ' + added + ', total keys: ' + Object.keys(sorted).length + ', remaining missing: ' + finalMissing.length);
}

