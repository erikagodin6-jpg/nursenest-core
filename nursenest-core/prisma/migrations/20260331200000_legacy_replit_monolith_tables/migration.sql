-- Legacy monolith tables required by Replit JSON import scripts.
-- Idempotent: safe to re-run; no TRUNCATE/DROP.

CREATE TABLE IF NOT EXISTS "content_translations" (
    "id" VARCHAR NOT NULL DEFAULT (gen_random_uuid())::text,
    "content_type" TEXT NOT NULL,
    "content_id" TEXT NOT NULL,
    "language_code" TEXT NOT NULL,
    "field_name" TEXT NOT NULL,
    "translated_text" TEXT NOT NULL,
    "translation_status" TEXT DEFAULT 'auto',
    "source_hash" TEXT,
    "source_last_updated_reference" TIMESTAMP(3),
    "last_updated" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "content_translations_pkey" PRIMARY KEY ("id")
);

CREATE UNIQUE INDEX IF NOT EXISTS "content_translations_natural_key_idx"
  ON "content_translations" ("content_type", "content_id", "field_name", "language_code");

