-- Prisma maps ContentItem → content_items, ExamQuestion → exam_questions.
-- Older migration history created Lesson/Question but not these tables; Prisma queries then fail at runtime.

CREATE TABLE IF NOT EXISTS "content_items" (
    "id" VARCHAR NOT NULL,
    "title" TEXT NOT NULL,
    "slug" TEXT NOT NULL,
    "type" TEXT NOT NULL DEFAULT 'lesson',
    "category" TEXT,
    "body_system" TEXT,
    "tier" TEXT,
    "status" TEXT DEFAULT 'draft',
    "tags" TEXT[] DEFAULT ARRAY[]::TEXT[],
    "summary" TEXT,
    "content" JSONB NOT NULL DEFAULT '[]',
    "seo_title" TEXT,
    "seo_description" TEXT,
    "seo_keywords" TEXT[] DEFAULT ARRAY[]::TEXT[],
    "primary_keyword" TEXT,
    "secondary_keywords" TEXT[] DEFAULT ARRAY[]::TEXT[],
    "scheduled_at" TIMESTAMP(3),
    "clinical_safety_review" BOOLEAN NOT NULL DEFAULT false,
    "auto_publish" BOOLEAN NOT NULL DEFAULT false,
    "created_at" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updated_at" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "published_at" TIMESTAMP(3),
    "author_id" TEXT,
    "author_name" TEXT,
    "region_scope" TEXT DEFAULT 'BOTH',
    "version_key" TEXT,
    "updated_by_ai" BOOLEAN NOT NULL DEFAULT false,
    "protected_fields" TEXT[] DEFAULT ARRAY[]::TEXT[],
    "source_version" INTEGER NOT NULL DEFAULT 1,

    CONSTRAINT "content_items_pkey" PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "exam_questions" (
    "id" VARCHAR NOT NULL,
    "tier" TEXT NOT NULL,
    "exam" TEXT NOT NULL,
    "question_type" TEXT NOT NULL,
    "status" TEXT DEFAULT 'draft',
    "publish_at" TIMESTAMP(3),
    "stem" TEXT NOT NULL,
    "options" JSONB DEFAULT '[]',
    "correct_answer" JSONB,
    "rationale" TEXT,
    "difficulty" INTEGER DEFAULT 3,
    "tags" TEXT[] DEFAULT ARRAY[]::TEXT[],
    "body_system" TEXT,
    "topic" TEXT,
    "subtopic" TEXT,
    "case_id" TEXT,
    "exhibit_data" JSONB,
    "region_scope" TEXT DEFAULT 'BOTH',
    "stem_hash" TEXT,
    "career_type" TEXT DEFAULT 'nursing',
    "scenario" TEXT,
    "clinical_pearl" TEXT,
    "exam_strategy" TEXT,
    "memory_hook" TEXT,
    "framework_used" TEXT,
    "clinical_trap" TEXT,
    "distractor_rationales" JSONB,
    "quality_scores" JSONB,
    "quality_feedback" JSONB,
    "quality_score" INTEGER,
    "country_code" TEXT,
    "region_code" TEXT,
    "licensing_body" TEXT,
    "language_code" TEXT DEFAULT 'en',
    "cognitive_level" TEXT,
    "question_format" TEXT,
    "is_scenario" BOOLEAN NOT NULL DEFAULT false,
    "is_mock_exam_eligible" BOOLEAN NOT NULL DEFAULT true,
    "is_adaptive_eligible" BOOLEAN NOT NULL DEFAULT true,
    "is_flashcard_source" BOOLEAN NOT NULL DEFAULT false,
    "is_study_guide_linked" BOOLEAN NOT NULL DEFAULT false,
    "is_tutor_ready" BOOLEAN NOT NULL DEFAULT false,
    "correct_answer_explanation" TEXT,
    "incorrect_answer_rationale" JSONB,
    "clinical_reasoning" TEXT,
    "key_takeaway" TEXT,
    "mnemonic" TEXT,
    "reference_source" TEXT,
    "lab_unit_variant" TEXT,
    "medication_naming_variant" TEXT,
    "case_context" TEXT,
    "vitals" JSONB,
    "labs" JSONB,
    "images" JSONB,
    "scenario_id" TEXT,
    "blueprint_weight" DOUBLE PRECISION,
    "created_at" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updated_at" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "published_at" TIMESTAMP(3),
    "source_version" INTEGER NOT NULL DEFAULT 1,

    CONSTRAINT "exam_questions_pkey" PRIMARY KEY ("id")
);

CREATE UNIQUE INDEX IF NOT EXISTS "content_items_slug_key" ON "content_items"("slug");
CREATE INDEX IF NOT EXISTS "content_items_type_status_idx" ON "content_items"("type", "status");
CREATE INDEX IF NOT EXISTS "exam_questions_status_tier_idx" ON "exam_questions"("status", "tier");
CREATE INDEX IF NOT EXISTS "exam_questions_stem_hash_idx" ON "exam_questions"("stem_hash");
