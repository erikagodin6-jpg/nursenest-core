#!/usr/bin/env node
/**
 * Lightweight Node heap guard for local npm scripts and CI logs.
 * - Logs the resolved NODE_OPTIONS (merges default heap when missing).
 * - Writes `scripts/.node-memory-exports.sh` so shells can `source` merged NODE_OPTIONS (npm `dev` / `build` / `start`).
 * - When `BUILD_NODE_MAX_OLD_SPACE_SIZE_MB` is set, it wins: any existing `--max-old-space-size=…` in
 *   `NODE_OPTIONS` is replaced (DigitalOcean may inject a larger heap; low-memory builds set 4096).
 * - When `BUILD_NODE_*` is unset, preserves an existing heap flag in `NODE_OPTIONS`; if none, defaults to 8192 MB.
 * - Warn-only when total RAM < 8 GiB (no behavior change).
 *
 * Idempotent: safe to run multiple times.
 */

import { writeFileSync } from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = fileURLToPath(new URL("..", import.meta.url));
const exportsPath = path.join(packageRoot, "scripts", ".node-memory-exports.sh");

const HEAP_RE = /--max-old-space-size=\d+/;
const DEFAULT_HEAP_MB = 8192;
const EIGHT_GIB = 8 * 1024 ** 3;

function heapFlagFromBuildEnvMb() {
  const raw = process.env.BUILD_NODE_MAX_OLD_SPACE_SIZE_MB;
  if (raw === undefined || raw === "") return null;
  const n = parseInt(String(raw).trim(), 10);
  if (!Number.isFinite(n) || n < 256 || n > 65536) return null;
  return `--max-old-space-size=${n}`;
}

function mergeNodeOptions() {
  const raw = String(process.env.NODE_OPTIONS ?? "").trim();
  const buildHeap = heapFlagFromBuildEnvMb();
  if (buildHeap) {
    const withoutHeap = raw.replace(HEAP_RE, "").replace(/\s+/g, " ").trim();
    return withoutHeap ? `${withoutHeap} ${buildHeap}` : buildHeap;
  }
  if (HEAP_RE.test(raw)) return raw;
  const heap = `--max-old-space-size=${DEFAULT_HEAP_MB}`;
  return raw ? `${raw} ${heap}` : heap;
}

function shSingleQuoted(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

const totalBytes = os.totalmem();
if (totalBytes < EIGHT_GIB) {
  console.warn("[node-memory] low memory environment detected");
}

const resolved = mergeNodeOptions();
console.log(`[node-memory] NODE_OPTIONS=${resolved}`);

const telemetryRaw = String(process.env.NEXT_TELEMETRY_DISABLED ?? "").trim();
const telemetry = telemetryRaw !== "" ? telemetryRaw : "1";

const singleWorkerRaw = String(process.env.NN_FORCE_SINGLE_BUILD_WORKER ?? "").trim();
const singleWorker = singleWorkerRaw !== "" ? singleWorkerRaw : "true";

const body =
  "# Generated by scripts/ensure-node-memory.mjs — safe to delete; regenerated on each run.\n" +
  `export NODE_OPTIONS=${shSingleQuoted(resolved)}\n` +
  `export NEXT_TELEMETRY_DISABLED=${shSingleQuoted(telemetry)}\n` +
  `export NN_FORCE_SINGLE_BUILD_WORKER=${shSingleQuoted(singleWorker)}\n`;

writeFileSync(exportsPath, body, { encoding: "utf8" });
