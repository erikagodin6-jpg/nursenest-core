function titleCase(input) {
  return input.replace(/\b[a-z]/g, (c) => c.toUpperCase()).replace(/\bAnd\b/g, "and").replace(/\bFor\b/g, "for");
}

function slugify(input) {
  return input.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "").slice(0, 72);
}

function section(id, kind, heading, body) {
  return { id, kind, heading, body };
}

function bodyText(title, system, focus, kind) {
  const base = {
    overview: `${title} belongs in RPN preparation because practical nurses frequently notice the first change in a patient's condition, reinforce the plan of care, and escalate concerns before preventable harm occurs. Learning objectives: explain the normal anatomy or physiology connected to ${focus}; identify common causes and expected findings; perform a focused nursing assessment; choose safe RPN interventions and reporting language; and teach the patient or family what to monitor after discharge. In ${examName} questions, the safest answer links the cue to airway, breathing, circulation, neurologic status, infection risk, medication safety, or functional decline. The RPN does not diagnose independently, but does collect meaningful data, compare trends, implement ordered and delegated care, and communicate clearly when the patient moves outside the expected range.`,
    patho: `Pathophysiology for ${title} starts with normal ${system.toLowerCase()} function, then asks what changed and why that change creates symptoms. In ${focus}, tissue oxygenation, perfusion, inflammation, fluid balance, nerve signaling, medication effect, or protective barriers may be altered. Compensatory responses can temporarily hide deterioration: heart rate rises before blood pressure falls, respiratory effort increases before oxygen saturation drops, confusion may appear before a dramatic vital sign change, and reduced intake or output can precede visible collapse. Practical nurses connect each sign to the mechanism so they can separate expected findings from trends needing reassessment, provider notification, or rapid response activation.`,
    symptoms: `Common findings for ${title} include the symptom pattern the patient reports, objective assessment data, and changes from baseline. Early signs are often subtle: new fatigue, mild shortness of breath, increased pain, appetite change, dizziness, reduced activity tolerance, skin changes, altered sleep, or a small shift in vital signs. Later warning signs include severe pain, cyanosis, syncope, acute confusion, refractory hypoxia, uncontrolled bleeding, fever with instability, new neurologic deficit, rapidly increasing swelling, or sharply reduced urine output. The RPN should ask what is new, what is worse, what is not responding to usual care, and what finding threatens safety. Trend-based recognition is especially important in older adults, pediatric patients, postpartum clients, and people with chronic disease.`,
    assessment: `Nursing assessment for ${title} should be focused, repeatable, and tied to risk. Start with ABCs, pain, mental status, vital signs, oxygen saturation when relevant, intake and output, skin and perfusion, medication timing, allergies, and the specific assessment for ${focus}. Compare current data with baseline and response after interventions. Interventions may include positioning, comfort measures, hygiene, mobility assistance, hydration support, ordered medication administration, specimen collection, device checks, infection prevention, and preparation for ordered diagnostics. Escalate with SBAR when findings worsen, red flags appear, treatment is not working, or the patient exceeds predictable RPN-managed care.`,
    education: `Patient education for ${title} should be concrete and teach-back based. Explain what the condition means in plain language, which symptoms to track, how medications should be taken, what side effects matter, and when to seek help. For ${focus}, teaching should include activity limits or progression, diet or fluid instructions when ordered, infection prevention, skin or device care, follow-up appointments, and how to report worsening symptoms. Ask the patient or caregiver to repeat the plan in their own words: what they will do today, what change means they should call the clinic, and what change means emergency care. Good RPN teaching reduces readmissions because the patient understands both the routine plan and the danger signs.`,
    pearls: `Clinical pearl: with ${title}, do not let a familiar chronic label make an acute change seem routine. The exam trap is choosing documentation, reassurance, or delayed teaching before reassessing the highest-risk cue. First decide whether oxygenation, perfusion, neurologic status, infection, bleeding, medication toxicity, or safety is threatened. Then act within RPN scope: assess, implement ordered care, protect the patient, and report promptly. Another common trap is treating a single normal value as proof of stability; trends and patient presentation matter more than one number. For ${examName}, the best answer is usually the one that recognizes deterioration early and closes the communication loop.`,
  };
  return base[kind];
}

