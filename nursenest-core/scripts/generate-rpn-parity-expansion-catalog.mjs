const lessons = [];
let index = 0;
for (const system of systems) {
  for (const concept of system.concepts) {
    lessons.push(buildLesson(system, concept, index++));
  }
}

const slugs = new Set();
for (const lesson of lessons) {
  if (slugs.has(lesson.slug)) throw new Error(`duplicate generated slug: ${lesson.slug}`);
  slugs.add(lesson.slug);
}

const out = {
  version: 1,
  generatedAt: "2026-05-02T00:00:00.000Z",
  source: "scripts/generate-rpn-parity-expansion-catalog.mjs",
  pathways: {
    [pathwayId]: lessons,
  },
};

fs.writeFileSync(outPath, `${JSON.stringify(out, null, 2)}\n`, "utf8");
