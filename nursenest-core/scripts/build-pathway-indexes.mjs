#!/usr/bin/env node
/**
 * 🏗️ Build Pathway Indexes
 *
 * Precomputes lightweight pathway metadata at build time to avoid
 * giant runtime hydration and oversized payloads.
 *
 * Generates:
 * - Pathway summaries (title, slug, description)
 * - Route maps (no full content)
 * - Category indexes
 * - Search indexes (compact)
 *
 * Output: src/generated/pathway-indexes/
 *
 * Usage: node scripts/build-pathway-indexes.mjs
 */

import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';

const OUTPUT_DIR = join(process.cwd(), 'src/generated/pathway-indexes');

// Ensure output directory exists
try {
  mkdirSync(OUTPUT_DIR, { recursive: true });
} catch (err) {
  // Directory exists
}

/**
 * Pathway metadata structure (lightweight)
 */
interface PathwayIndex {
  slug: string;
  title: string;
  description: string;
  category: string;
  tier: 'rn' | 'rpn' | 'np' | 'allied' | 'pre-nursing';
  lessonCount: number;
  flashcardCount: number;
  practiceQuestionCount: number;
  href: string;
}

/**
 * Build RN pathway indexes
 */
function buildRNIndexes(): PathwayIndex[] {
  // In production, this would query the database or content files
  // For now, return structure showing the pattern
  return [
    {
      slug: 'nclex-rn',
      title: 'NCLEX-RN Prep',
      description: 'Comprehensive NCLEX-RN preparation',
      category: 'nursing',
      tier: 'rn',
      lessonCount: 150,
      flashcardCount: 2000,
      practiceQuestionCount: 5000,
      href: '/nclex-rn',
    },
    // Add more pathways...
  ];
}

/**
 * Build RPN pathway indexes
 */
function buildRPNIndexes(): PathwayIndex[] {
  return [
    {
      slug: 'rex-pn',
      title: 'REX-PN Prep',
      description: 'Canadian practical nurse exam preparation',
      category: 'nursing',
      tier: 'rpn',
      lessonCount: 120,
      flashcardCount: 1500,
      practiceQuestionCount: 3000,
      href: '/rex-pn',
    },
  ];
}

/**
 * Build NP pathway indexes
 */
function buildNPIndexes(): PathwayIndex[] {
  return [
    {
      slug: 'cnple',
      title: 'CNPLE Prep',
      description: 'Canadian nurse practitioner exam preparation',
      category: 'advanced-practice',
      tier: 'np',
      lessonCount: 100,
      flashcardCount: 1200,
      practiceQuestionCount: 2500,
      href: '/np/cnple',
    },
  ];
}

/**
 * Build category indexes
 */
function buildCategoryIndexes(pathways: PathwayIndex[]) {
  const categories = new Map<string, PathwayIndex[]>();
  
  pathways.forEach(pathway => {
    if (!categories.has(pathway.category)) {
      categories.set(pathway.category, []);
    }
    categories.get(pathway.category)!.push(pathway);
  });
  
  return Object.fromEntries(categories);
}

/**
 * Build search index (compact)
 */
function buildSearchIndex(pathways: PathwayIndex[]) {
  return pathways.map(p => ({
    slug: p.slug,
    title: p.title,
    tier: p.tier,
    href: p.href,
    // Searchable text (lowercase for case-insensitive search)
    searchText: `${p.title} ${p.description}`.toLowerCase(),
  }));
}

/**
 * Main build function
 */
function buildIndexes() {
  console.log('🏗️  Building pathway indexes...\n');
  
  // Build tier-specific indexes
  const rnPathways = buildRNIndexes();
  const rpnPathways = buildRPNIndexes();
  const npPathways = buildNPIndexes();
  
  const allPathways = [...rnPathways, ...rpnPathways, ...npPathways];
  
  console.log(`Total pathways: ${allPathways.length}`);
  console.log(`  RN: ${rnPathways.length}`);
  console.log(`  RPN: ${rpnPathways.length}`);
  console.log(`  NP: ${npPathways.length}\n`);
  
  // Build category indexes
  const categoryIndexes = buildCategoryIndexes(allPathways);
  console.log(`Categories: ${Object.keys(categoryIndexes).length}\n`);
  
  // Build search index
  const searchIndex = buildSearchIndex(allPathways);
  
  // Write indexes
  writeFileSync(
    join(OUTPUT_DIR, 'rn-pathways.json'),
    JSON.stringify(rnPathways, null, 2)
  );
  
  writeFileSync(
    join(OUTPUT_DIR, 'rpn-pathways.json'),
    JSON.stringify(rpnPathways, null, 2)
  );
  
  writeFileSync(
    join(OUTPUT_DIR, 'np-pathways.json'),
    JSON.stringify(npPathways, null, 2)
  );
  
  writeFileSync(
    join(OUTPUT_DIR, 'all-pathways.json'),
    JSON.stringify(allPathways, null, 2)
  );
  
  writeFileSync(
    join(OUTPUT_DIR, 'categories.json'),
    JSON.stringify(categoryIndexes, null, 2)
  );
  
  writeFileSync(
    join(OUTPUT_DIR, 'search-index.json'),
    JSON.stringify(searchIndex, null, 2)
  );
  
  // Generate TypeScript types
  const typesContent = `/**
 * Generated pathway index types
 * DO NOT EDIT - Generated by scripts/build-pathway-indexes.mjs
 */

export interface PathwayIndex {
  slug: string;
  title: string;
  description: string;
  category: string;
  tier: 'rn' | 'rpn' | 'np' | 'allied' | 'pre-nursing';
  lessonCount: number;
  flashcardCount: number;
  practiceQuestionCount: number;
  href: string;
}

export interface SearchIndexEntry {
  slug: string;
  title: string;
  tier: string;
  href: string;
  searchText: string;
}
`;
  
  writeFileSync(join(OUTPUT_DIR, 'types.ts'), typesContent);
  
  console.log('✅ Indexes built successfully\n');
  console.log('Output files:');
  console.log('  - rn-pathways.json');
  console.log('  - rpn-pathways.json');
  console.log('  - np-pathways.json');
  console.log('  - all-pathways.json');
  console.log('  - categories.json');
  console.log('  - search-index.json');
  console.log('  - types.ts\n');
  
  console.log('Usage in components:');
  console.log('  import pathways from "@/generated/pathway-indexes/rn-pathways.json";\n');
}

// Run build
try {
  buildIndexes();
} catch (error) {
  console.error('❌ Build failed:', error);
  process.exit(1);
}
