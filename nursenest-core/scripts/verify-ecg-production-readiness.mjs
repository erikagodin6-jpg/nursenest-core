#!/usr/bin/env node
/**
 * ECG production readiness gate.
 *
 * This is a pre-publish safety check for ECG surfaces. It intentionally checks
 * the things that damage learner trust fastest:
 *   - exposed secrets/debug metadata on client/marketing/module surfaces
 *   - placeholder or AI-ish unfinished copy markers
 *   - adult/pediatric/neonatal separation evidence
 *   - clinical review metadata for advanced/pediatric content
 *   - production access/publish guard files
 *
 * Usage:
 *   node scripts/verify-ecg-production-readiness.mjs
 */

import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const root = process.cwd();

const REQUIRED_FILES = [
  "src/lib/ecg-module/ecg-module-status.ts",
  "src/lib/ecg-module/ecg-module.server.ts",
  "src/lib/ecg-module/ecg-learner-mastery.ts",
  "src/lib/ecg-module/ecg-question-store.ts",
  "src/lib/ecg-module/ecg-telemetry.ts",
  "src/lib/ecg-module/ecg-pediatric-governance.ts",
  "src/lib/ecg-module/ecg-pediatric-curriculum.ts",
  "src/lib/ecg-module/ecg-pediatric-rhythm-registry.ts",
  "src/lib/ecg-module/ecg-premium-curated-pack.ts",
  "src/lib/advanced-ecg/advanced-ecg-access.ts",
  "src/components/study/ecg-live-strip.tsx",
  "src/app/modules/ecg/basic/worksheets/page.tsx",
  "src/app/modules/ecg/advanced/lessons/page.tsx",
  "src/app/modules/ecg-advanced/layout.tsx",
  "src/app/modules/ecg-interpretation/quiz/page.tsx",
  "src/app/(marketing)/(default)/ecg-practice-questions/page.tsx",
  "src/app/(marketing)/(default)/ecg/[topic]/page.tsx",
  "src/lib/ecg-module/ecg-seo-cluster.ts",
  "scripts/check-ecg-publish-env.mjs",
];

const SCAN_ROOTS = [
  "src/app/modules/ecg",
  "src/app/modules/ecg-advanced",
  "src/app/modules/ecg-interpretation",
  "src/app/(marketing)/(default)/ecg-practice-questions",
  "src/app/(marketing)/(default)/ecg",
  "src/components/study",
  "src/lib/ecg-module",
  "src/lib/advanced-ecg",
];

const ALLOWED_EXTENSIONS = new Set([".ts", ".tsx", ".js", ".jsx", ".md", ".mdx", ".json"]);

const SECRET_PATTERNS = [
  { name: "Stripe secret key", re: /sk_(live|test)_[A-Za-z0-9]{16,}/g },
  { name: "Stripe webhook secret", re: /whsec_[A-Za-z0-9]{16,}/g },
  { name: "GitHub token", re: /gh[pousr]_[A-Za-z0-9_]{20,}/g },
  { name: "JWT-looking secret", re: /eyJ[A-Za-z0-9_-]{20,}\.[A-Za-z0-9_-]{20,}\.[A-Za-z0-9_-]{10,}/g },
  { name: "Database URL", re: /postgres(?:ql)?:\/\/[^\s"'`]+/g },
  { name: "Private key block", re: /-----BEGIN (?:RSA |EC |OPENSSH |)PRIVATE KEY-----/g },
  { name: "AWS access key", re: /AKIA[0-9A-Z]{16}/g },
];

const CLIENT_DEBUG_PATTERNS = [
  { name: "raw process.env usage", re: /process\.env\.(?!NEXT_PUBLIC_)[A-Z0-9_]+/g },
  { name: "console debug/log left in ECG surface", re: /console\.(debug|log|trace)\(/g },
  { name: "debug JSON rendering", re: /JSON\.stringify\([^)]*(env|secret|token|config|process)/gi },
  { name: "visible implementation metadata", re: /(internalCourseId|databaseUrl|stripeSecret|authSecret|QA_PAID_PASSWORD|QA_PAID_EMAIL)/g },
];

const PLACEHOLDER_PATTERNS = [
  { name: "TODO marker", re: /\bTODO\b/gi },
  { name: "FIXME marker", re: /\bFIXME\b/gi },
  { name: "Lorem ipsum", re: /lorem ipsum/gi },
  { name: "Placeholder text", re: /placeholder(?: copy| text)?/gi },
  { name: "Coming soon on production surface", re: /coming soon/gi },
  { name: "AI-generated wording", re: /AI[- ]generated|generated by AI|as an AI/gi },
  { name: "Template filler", re: /\bTBD\b|to be written|insert (?:copy|content|lesson)/gi },
];

const CLINICAL_REQUIRED_TERMS = [
  { file: "src/lib/ecg-module/ecg-pediatric-governance.ts", terms: ["clinicalReviewStatus", "reviewed", "pals_acls_isolation", "pediatric", "adult"] },
  { file: "src/lib/ecg-module/ecg-pediatric-curriculum.ts", terms: ["clinicalReviewStatus", "reviewed", "pediatric", "neonate"] },
  { file: "src/lib/ecg-module/ecg-pediatric-rhythm-registry.ts", terms: ["neonate", "infant", "child", "adolescent"] },
  { file: "src/lib/ecg-module/ecg-telemetry.ts", terms: ["telemetry", "rhythm"] },
  { file: "src/lib/ecg-module/ecg-premium-curated-pack.ts", terms: ["rationale", "rhythm"] },
  { file: "src/lib/advanced-ecg/advanced-ecg-access.ts", terms: ["advanced", "entitlement"] },
];

function exists(relPath) {
  return fs.existsSync(path.join(root, relPath));
}

function read(relPath) {
  return fs.readFileSync(path.join(root, relPath), "utf8");
}

function walk(dir, out = []) {
  const abs = path.join(root, dir);
  if (!fs.existsSync(abs)) return out;
  for (const entry of fs.readdirSync(abs, { withFileTypes: true })) {
    if (entry.name.startsWith(".")) continue;
    const rel = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(rel, out);
    } else if (ALLOWED_EXTENSIONS.has(path.extname(entry.name))) {
      out.push(rel);
    }
  }
  return out;
}

function collectScannedFiles() {
  return [...new Set(SCAN_ROOTS.flatMap((dir) => walk(dir)))].sort();
}

function matchPattern(file, content, pattern) {
  const matches = [];
  pattern.re.lastIndex = 0;
  let m;
  while ((m = pattern.re.exec(content)) !== null) {
    const before = content.slice(0, m.index);
    const line = before.split(/\r?\n/).length;
    const excerpt = content.slice(Math.max(0, m.index - 40), Math.min(content.length, m.index + 80)).replace(/\s+/g, " ");
    matches.push({ file, line, excerpt });
    if (m.index === pattern.re.lastIndex) pattern.re.lastIndex += 1;
  }
  return matches;
}

const failures = [];
const warnings = [];

for (const rel of REQUIRED_FILES) {
  if (!exists(rel)) {
    failures.push({ check: "required_file", message: `Missing required ECG production file: ${rel}` });
  }
}

const scannedFiles = collectScannedFiles();
if (scannedFiles.length < 10) {
  failures.push({ check: "scan_scope", message: `ECG scan scope found only ${scannedFiles.length} files; expected production ECG surfaces to exist.` });
}

for (const file of scannedFiles) {
  const content = read(file);
  for (const pattern of SECRET_PATTERNS) {
    for (const hit of matchPattern(file, content, pattern)) {
      failures.push({ check: "secret_leak", message: `${pattern.name} found in ${hit.file}:${hit.line}`, excerpt: hit.excerpt });
    }
  }
  for (const pattern of CLIENT_DEBUG_PATTERNS) {
    for (const hit of matchPattern(file, content, pattern)) {
      failures.push({ check: "debug_or_internal_leak", message: `${pattern.name} found in ${hit.file}:${hit.line}`, excerpt: hit.excerpt });
    }
  }
  for (const pattern of PLACEHOLDER_PATTERNS) {
    for (const hit of matchPattern(file, content, pattern)) {
      failures.push({ check: "placeholder_or_ai_copy", message: `${pattern.name} found in ${hit.file}:${hit.line}`, excerpt: hit.excerpt });
    }
  }
}

for (const req of CLINICAL_REQUIRED_TERMS) {
  if (!exists(req.file)) continue;
  const content = read(req.file).toLowerCase();
  for (const term of req.terms) {
    if (!content.includes(term.toLowerCase())) {
      failures.push({ check: "clinical_review_evidence", message: `${req.file} is missing required clinical-readiness term: ${term}` });
    }
  }
}

// Require visible separation of basic and advanced ECG access/surfaces.
const basicSurface = "src/app/modules/ecg/basic/worksheets/page.tsx";
const advancedSurface = "src/app/modules/ecg/advanced/lessons/page.tsx";
if (exists(basicSurface) && exists(advancedSurface)) {
  const basic = read(basicSurface).toLowerCase();
  const advanced = read(advancedSurface).toLowerCase();
  if (!basic.includes("basic") && !basic.includes("foundation")) {
    failures.push({ check: "basic_advanced_separation", message: `${basicSurface} should clearly present Basic/Foundation ECG as distinct from Advanced ECG.` });
  }
  if (!advanced.includes("advanced") && !advanced.includes("premium")) {
    failures.push({ check: "basic_advanced_separation", message: `${advancedSurface} should clearly present Advanced/Premium ECG as distinct from Basic ECG.` });
  }
}

// Guard that publish env checker does not print actual sensitive values.
const publishChecker = "scripts/check-ecg-publish-env.mjs";
if (exists(publishChecker)) {
  const checker = read(publishChecker);
  if (/value \"\$\{val\}\"/.test(checker) || /console\.log\([^)]*process\.env/.test(checker)) {
    warnings.push({ check: "env_output", message: `${publishChecker} may print environment values. Prefer redacted output for all non-boolean vars.` });
  }
}

const summary = {
  scannedFiles: scannedFiles.length,
  failures: failures.length,
  warnings: warnings.length,
};

console.log("\n=== ECG Production Readiness Gate ===");
console.log(JSON.stringify(summary, null, 2));

if (warnings.length) {
  console.log("\nWarnings:");
  for (const w of warnings) console.log(`  ⚠️  [${w.check}] ${w.message}`);
}

if (failures.length) {
  console.log("\nFailures:");
  for (const f of failures) {
    console.log(`  ❌ [${f.check}] ${f.message}`);
    if (f.excerpt) console.log(`     ${f.excerpt}`);
  }
  console.log("\nECG is NOT production-ready. Fix failures before publishing.\n");
  process.exit(1);
}

console.log("\n✅ ECG production readiness gate passed. Proceed to clinical SME review and release QA.\n");
process.exit(0);
