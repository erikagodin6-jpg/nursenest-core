#!/usr/bin/env node
/**
 * Phase 1B: hydration / bundle-risk report for selected large client islands.
 *
 * - Always writes `reports/hydration-risk-routes.md` + `reports/hydration-risk-routes.json`.
 * - **Warn-only** when `reports/hydration-risk-baseline.json` is missing (exit 0).
 * - When baseline exists: **exit 1** only on **new critical** risks — files whose current rank is
 *   `high` (risk score ≥ 4500) that were **not** listed as `high` in the baseline snapshot.
 *
 * Bootstrap / refresh baseline after intentional growth:
 *   node scripts/report-hydration-risk-routes.mjs --write-baseline
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const coreRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const reportMdPath = path.join(coreRoot, "reports", "hydration-risk-routes.md");
const reportJsonPath = path.join(coreRoot, "reports", "hydration-risk-routes.json");
const baselinePath = path.join(coreRoot, "reports", "hydration-risk-baseline.json");

const TARGETS = [
  "src/components/student/practice-test-runner-client.tsx",
  "src/components/student/practice-tests-hub-client.tsx",
  "src/components/student/question-bank-practice-client.tsx",
  "src/components/admin/admin-blog-control-panel-client.tsx",
  "src/app/(app)/app/(learner)/layout.tsx",
];

const CRITICAL_SCORE = 4500;

function staticImportSpecifiers(source) {
  const out = [];
  const re = /^\s*import\s+[^'"]*\s+from\s+['"]([^'"]+)['"]\s*;?/gm;
  let m;
  while ((m = re.exec(source)) !== null) out.push(m[1]);
  return out;
}

function analyze(rel) {
  const abs = path.join(coreRoot, rel);
  if (!fs.existsSync(abs)) return { rel, missing: true };
  const raw = fs.readFileSync(abs, "utf8");
  const lines = raw.split(/\r?\n/).length;
  const bytes = Buffer.byteLength(raw, "utf8");
  const head = raw.slice(0, 12000);
  const isUseClient = /^\s*["']use client["']\s*;/m.test(head);
  const imports = staticImportSpecifiers(head).slice(0, 40);
  const riskScore = lines * (isUseClient ? 1.15 : 0.85);
  return { rel, lines, bytes, isUseClient, imports, riskScore, missing: false };
}

function riskRankLabel(score) {
  if (score >= CRITICAL_SCORE) return "high";
  if (score >= 2500) return "medium";
  return "lower";
}

function readBaseline() {
  if (!fs.existsSync(baselinePath)) return null;
  try {
    const raw = fs.readFileSync(baselinePath, "utf8");
    const j = JSON.parse(raw);
    const criticalRels = Array.isArray(j.criticalRels) ? j.criticalRels.filter((x) => typeof x === "string") : [];
    return { version: j.version ?? 1, criticalRels: new Set(criticalRels) };
  } catch {
    return null;
  }
}

function main() {
  const writeBaseline = process.argv.includes("--write-baseline");
  const rows = TARGETS.map(analyze).sort((a, b) => (b.riskScore ?? 0) - (a.riskScore ?? 0));
  const jsonRows = rows.map((r) => {
    if (r.missing) return { rel: r.rel, missing: true };
    const risk = riskRankLabel(r.riskScore);
    return {
      rel: r.rel,
      lines: r.lines,
      bytes: r.bytes,
      useClient: r.isUseClient,
      riskScore: Math.round(r.riskScore * 100) / 100,
      risk,
    };
  });

  const md = [];
  md.push("# Hydration-risk route telemetry (Phase 1B)", "");
  md.push("Generated by `npm run report:hydration-risk-routes`.", "");
  md.push("## Baseline gating (CI)", "");
  md.push(
    "- **Critical** = heuristic rank `high` (internal score ≥ " + CRITICAL_SCORE + ").",
    "",
    "- If `reports/hydration-risk-baseline.json` is **missing**: warn on stderr, **exit 0**.",
    "- If baseline **exists**: **exit 1** when any current `high` file is **not** in `criticalRels` (new critical risk).",
    "- Refresh after review: `node scripts/report-hydration-risk-routes.mjs --write-baseline`",
    "",
  );
  md.push("## Learner shell guardrail", "");
  md.push("Run `npm run test:learner-shell-imports` for blocked static imports.", "");
  md.push("## Target files", "");
  md.push("| Rank | File | Lines | Bytes | use client | Risk |");
  md.push("| --- | --- | ---: | ---: | --- | --- |");
  let i = 1;
  for (const r of rows) {
    if (r.missing) md.push("| " + i + " | " + r.rel + " | — | — | — | missing |");
    else md.push("| " + i + " | `" + r.rel + "` | " + r.lines + " | " + r.bytes + " | " + (r.isUseClient ? "yes" : "no") + " | " + riskRankLabel(r.riskScore) + " |");
    i += 1;
  }
  md.push("", "## Static imports (first 12k chars)", "");
  for (const r of rows) {
    if (r.missing) continue;
    md.push("### `" + r.rel + "`", "");
    md.push(r.isUseClient ? "- Boundary: Client Component." : "- Boundary: Server layout.", "");
    md.push("- Imports:", ...r.imports.map((s) => "  - `" + s + "`"), "");
  }
  md.push("", "*Generated " + new Date().toISOString() + "*", "");
  fs.mkdirSync(path.dirname(reportMdPath), { recursive: true });
  fs.writeFileSync(reportMdPath, md.join("\n"), "utf8");
  fs.writeFileSync(reportJsonPath, JSON.stringify({ generatedAt: new Date().toISOString(), rows: jsonRows }, null, 2), "utf8");
  console.log("[report:hydration-risk-routes] wrote " + path.relative(coreRoot, reportMdPath));
  console.log("[report:hydration-risk-routes] wrote " + path.relative(coreRoot, reportJsonPath));

  const currentCritical = new Set();
  for (const r of rows) {
    if (r.missing) continue;
    const risk = riskRankLabel(r.riskScore);
    console.error(
      "[nursenest-core] hydration_risk_route_row " +
        JSON.stringify({ rel: r.rel, lines: r.lines, bytes: r.bytes, useClient: r.isUseClient, risk }),
    );
    if (risk === "high") currentCritical.add(r.rel);
  }

  const criticalList = [...currentCritical].sort();

  if (writeBaseline) {
    const payload = {
      version: 1,
      criticalRels: criticalList,
      criticalScoreThreshold: CRITICAL_SCORE,
      updatedAt: new Date().toISOString(),
      note: "Regenerate with: node scripts/report-hydration-risk-routes.mjs --write-baseline",
    };
    fs.writeFileSync(baselinePath, JSON.stringify(payload, null, 2), "utf8");
    console.log("[report:hydration-risk-routes] wrote baseline " + path.relative(coreRoot, baselinePath));
    return;
  }

  const baseline = readBaseline();
  if (!baseline) {
    console.warn(
      "[report:hydration-risk-routes] WARN: no baseline at reports/hydration-risk-baseline.json — new critical risks are warn-only. Commit a baseline after review (`--write-baseline`).",
    );
    return;
  }

  const newCritical = criticalList.filter((rel) => !baseline.criticalRels.has(rel));
  if (newCritical.length > 0) {
    console.error("[report:hydration-risk-routes] FAIL: new critical hydration-risk targets:", newCritical.join(", "));
    process.exitCode = 1;
  }
}

main();
