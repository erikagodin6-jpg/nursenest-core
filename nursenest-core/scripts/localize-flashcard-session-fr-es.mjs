function patch(file, overrides) {
  const p = path.join(i18nDir, file);
  const j = JSON.parse(fs.readFileSync(p, "utf8"));
  Object.assign(j, overrides);
  fs.writeFileSync(p, JSON.stringify(j));
  console.log("patched", file, Object.keys(overrides).length, "keys");
}

