export function auditMeasurementGovernance(): {
  governed: number;
  partial: number;
  ungoverned: number;
  blocked: number;
  orchestrated: number;
  aiGoverned: number;
  surfaces: MeasurementConsumerSurface[];
} {
  const governed = MEASUREMENT_CONSUMER_REGISTRY.filter((s) => s.status === "governed").length;
  const partial = MEASUREMENT_CONSUMER_REGISTRY.filter((s) => s.status === "partial").length;
  const ungoverned = MEASUREMENT_CONSUMER_REGISTRY.filter((s) => s.status === "ungoverned").length;
  const blocked = MEASUREMENT_CONSUMER_REGISTRY.filter((s) => s.status === "blocked").length;
  const orchestrated = MEASUREMENT_CONSUMER_REGISTRY.filter((s) => s.usesOrchestrator).length;
  const aiGoverned = MEASUREMENT_CONSUMER_REGISTRY.filter((s) => s.usesAiGovernance).length;
  return {
    governed,
    partial,
    ungoverned,
    blocked,
    orchestrated,
    aiGoverned,
    surfaces: MEASUREMENT_CONSUMER_REGISTRY,
  };
}

export function listUngovernedSurfaces(): MeasurementConsumerSurface[] {
  return MEASUREMENT_CONSUMER_REGISTRY.filter(
    (s) => s.status === "ungoverned" || s.status === "partial",
  );
}
