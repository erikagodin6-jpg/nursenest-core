import type { Prisma } from "@prisma/client";
import { defaultEcgStripConfigForRhythm } from "@/lib/ecg-module/ecg-waveform-generator";
import type { EcgStripMediaConfig } from "@/lib/ecg-module/ecg-waveform-generator";

const REVIEWED_AT = new Date("2026-05-12T12:00:00.000Z");
const REVIEWER = "advanced-ecg-curated-pack-v1";

type AdvancedEcgRowArgs = {
  id: string;
  rhythmKey: string;
  rhythmTag?: string;
  stem: string;
  options: Array<{ id: string; text: string }>;
  correctAnswerId: string;
  rationale: string;
  category: string;
  clinicalPriority?: string | null;
  mediaPatch?: Partial<EcgStripMediaConfig>;
  lessonSlug?: string;
};

function four(a: string, b: string, c: string, d: string): Array<{ id: string; text: string }> {
  return [
    { id: "a", text: a },
    { id: "b", text: b },
    { id: "c", text: c },
    { id: "d", text: d },
  ];
}

function stripCfg(rhythmKey: string, patch: Partial<EcgStripMediaConfig> = {}): EcgStripMediaConfig {
  const base = defaultEcgStripConfigForRhythm(rhythmKey);
  return { ...base, ...patch, mediaType: "ecg_live_strip", manualReviewed: true, manuallyReviewedAt: "2026-05-12" };
}

function makeAdvancedRow(args: AdvancedEcgRowArgs): Prisma.EcgVideoQuestionCreateInput {
  const { id, rhythmKey, rhythmTag, stem, options, correctAnswerId, rationale, category, clinicalPriority, mediaPatch, lessonSlug } = args;
  const cfg = stripCfg(rhythmKey, mediaPatch);
  const topicTags = ["ecg", "advanced", category, rhythmKey, ...(lessonSlug ? [`lesson:${lessonSlug}`, `remediation:${lessonSlug}`] : [])];
  return {
    id,
    videoUrl: "",
    thumbnailUrl: null,
    durationSeconds: null,
    mediaType: "ecg_live_strip",
    mediaConfig: cfg as unknown as Prisma.InputJsonValue,
    questionText: stem,
    answerOptions: options as unknown as Prisma.InputJsonValue,
    correctAnswerId,
    rationale,
    difficulty: "advanced",
    rhythmTag: rhythmTag ?? rhythmKey,
    clinicalPriority: clinicalPriority ?? null,
    allowedTiers: ["RN", "NP"],
    isPremium: true,
    level: "advanced",
    mode: "lesson",
    topicTags,
    lessonLinkCount: lessonSlug ? 1 : 0,
    medicalQaStatus: "approved",
    manualReviewedAt: REVIEWED_AT,
    manualReviewedBy: REVIEWER,
    clinicianReviewedAt: REVIEWED_AT,
    clinicianReviewedBy: REVIEWER,
    waveformFidelity: "morphology_approximate",
    qaStatus: "approved",
    publishSafetyStatus: "safe",
  };
}

