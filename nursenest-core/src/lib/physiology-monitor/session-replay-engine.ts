/**
 * Session Replay Engine
 *
 * Transforms recorded PhysiologySnapshot[] + ScoringEvent[] + HarmEvent[]
 * into a fully-seekable replay timeline that powers the replay player UI.
 *
 * The engine operates purely on snapshot data — no live MonitorEngine instance
 * is needed during replay. This means sessions can be replayed from stored
 * JSON months after they occurred.
 *
 * Key affordances:
 *   - Jump to any tick (seekTo)
 *   - Jump to named critical moments (seekToEvent)
 *   - Step forward/back one tick
 *   - Compare learner path vs. optimal path at each tick
 *   - Annotate missed-opportunity windows
 *   - Export timeline summary for institutional review
 */

import type { PhysiologySnapshot, PhysiologyState } from "./physiology-state";
import type { ScoringEvent } from "./monitor-engine";
import type { HarmEvent } from "./harm-index";
import type { NcjmmDomain } from "./clinical-judgment-engine";
import { SIM_SECONDS_PER_TICK } from "./monitor-engine";

// ─── Timeline event types ─────────────────────────────────────────────────────

export type ReplayEventKind =
  | "stage_transition"    // condition stage changed
  | "intervention"        // learner applied intervention
  | "recognition"         // learner recorded recognition event
  | "escalation"          // escalation to provider / rapid response
  | "alarm_fired"         // critical alarm became active
  | "harm_event"          // harm threshold crossed
  | "missed_opportunity"  // indicated action not taken within window
  | "optimal_action"      // action was taken in optimal window
  | "session_start"
  | "session_end";

export interface ReplayEvent {
  tick: number;
  simSeconds: number;
  kind: ReplayEventKind;
  label: string;
  detail: string;
  /** For scoring events: the NCJMM domain credited. */
  ncjmmDomain?: NcjmmDomain;
  /** Severity for UI colouring. */
  severity: "info" | "warning" | "critical" | "success";
  /** Whether this was an optimal action (green), delayed (amber), missed (red). */
  quality?: "optimal" | "delayed" | "missed";
  /** Score contribution (+N or −N). */
  scoreContribution?: number;
}

// ─── Stage transition record ──────────────────────────────────────────────────

export interface StageTransition {
  tick: number;
  simSeconds: number;
  from: PhysiologyState["conditionStage"];
  to: PhysiologyState["conditionStage"];
  /** True if the learner had applied any intervention before this transition. */
  hadIntervened: boolean;
  /** Vital that most clearly indicated this transition. */
  keyVital: string;
}

// ─── Missed opportunity ───────────────────────────────────────────────────────

export interface MissedOpportunity {
  tick: number;
  simSeconds: number;
  description: string;
  /** What the learner should have done. */
  expectedAction: string;
  /** How delayed the actual action was (or null if never taken). */
  delayTicks: number | null;
  isHarm: boolean;
}

// ─── Optimal path comparison ─────────────────────────────────────────────────

export interface OptimalPathComparison {
  tick: number;
  simSeconds: number;
  optimalAction: string;
  learnerAction: string | null;
  timingDeltaTicks: number | null;    // positive = learner was late, negative = early
  wasCorrect: boolean;
  coachingNote: string;
}

// ─── Full replay timeline ─────────────────────────────────────────────────────

export interface ReplayTimeline {
  /** Session identifier. */
  sessionId: string;
  conditionKey: string;
  /** All snapshots in tick order. */
  snapshots: PhysiologySnapshot[];
  /** All named events (interventions, recognitions, alarms, stage transitions). */
  events: ReplayEvent[];
  /** Stage transitions with context. */
  stageTransitions: StageTransition[];
  /** Windows where a required action was missed or delayed. */
  missedOpportunities: MissedOpportunity[];
  /** Per-tick comparison of learner vs optimal path. */
  optimalPathComparisons: OptimalPathComparison[];
  /** Bookmarks auto-placed at key moments (can be user-augmented). */
  bookmarks: ReplayBookmark[];
  /** Total ticks in the replay. */
  totalTicks: number;
  /** Total simulation seconds. */
  totalSimSeconds: number;
  /** Pre-computed per-tick alarm counts for the timeline scrubber. */
  alarmCountByTick: number[];
  /** Pre-computed deterioration severity (0=stable, 1=warning, 2=critical) per tick. */
  severityByTick: Array<0 | 1 | 2>;
}

export interface ReplayBookmark {
  tick: number;
  simSeconds: number;
  label: string;
  kind: "critical_alarm" | "intervention" | "stage_change" | "harm" | "missed" | "optimal";
  autoGenerated: boolean;
}

// ─── Replay cursor (returned from seek operations) ───────────────────────────

export interface ReplayCursor {
  tick: number;
  simSeconds: number;
  /** The snapshot at this tick (physiology state for display). */
  snapshot: PhysiologySnapshot;
  /** Events that occurred AT this tick. */
  eventsAtTick: ReplayEvent[];
  /** Active interventions at this tick (from snapshot state). */
  activeInterventions: string[];
  /** Current stage at this tick. */
  conditionStage: PhysiologyState["conditionStage"];
  /** Alarms active at this tick (derived from snapshot). */
  alarmLabels: string[];
  /** Commentary for the educational overlay at this tick. */
  educationalNote: string | null;
}

// ─── Builder ──────────────────────────────────────────────────────────────────

/**
 * Build a ReplayTimeline from raw session data.
 *
 * This is called once after a session ends (or when loading a stored session).
 * The result can be JSON-serialised and stored alongside the session report.
 */
export function buildReplayTimeline(input: {
  sessionId: string;
  conditionKey: string;
  snapshots: PhysiologySnapshot[];
  scoringEvents: ScoringEvent[];
  harmEvents?: HarmEvent[];
  conditionLabel?: string;
}): ReplayTimeline {
  const { sessionId, conditionKey, snapshots, scoringEvents, harmEvents = [] } = input;

  if (snapshots.length === 0) {
    return emptyTimeline(sessionId, conditionKey);
  }

  const totalTicks = snapshots[snapshots.length - 1]!.tick;
  const totalSimSeconds = totalTicks * SIM_SECONDS_PER_TICK;

  // ── Stage transitions ──────────────────────────────────────────────────────
  const stageTransitions: StageTransition[] = [];
  for (let i = 1; i < snapshots.length; i++) {
    const prev = snapshots[i - 1]!;
    const curr = snapshots[i]!;
    if (curr.state.conditionStage !== prev.state.conditionStage) {
      const hadIntervened = scoringEvents.some(
        (e) => e.type === "intervention" && e.tick < curr.tick,
      );
      stageTransitions.push({
        tick: curr.tick,
        simSeconds: curr.simSeconds,
        from: prev.state.conditionStage,
        to: curr.state.conditionStage,
        hadIntervened,
        keyVital: detectKeyVital(prev.state, curr.state),
      });
    }
  }

  // ── Replay events from scoring events ─────────────────────────────────────
  const replayEvents: ReplayEvent[] = [
    {
      tick: 0, simSeconds: 0,
      kind: "session_start",
      label: "Session started",
      detail: `Condition: ${conditionKey}`,
      severity: "info",
    },
  ];

  for (const ev of scoringEvents) {
    replayEvents.push({
      tick: ev.tick,
      simSeconds: ev.simSeconds,
      kind: ev.type === "intervention" ? "intervention"
        : ev.type === "recognition" ? "recognition"
        : "escalation",
      label: ev.detail,
      detail: ev.detail,
      severity: ev.type === "recognition" ? "success"
        : ev.type === "escalation" ? "warning"
        : "info",
      quality: scoringEventQuality(ev, stageTransitions),
      scoreContribution: ev.score,
    });
  }

  // Add stage transition events
  for (const st of stageTransitions) {
    replayEvents.push({
      tick: st.tick,
      simSeconds: st.simSeconds,
      kind: "stage_transition",
      label: `Condition advanced to ${st.to}`,
      detail: `Stage changed from ${st.from} to ${st.to}. ${st.hadIntervened ? "Interventions had been applied." : "No interventions yet."}`,
      severity: st.to === "critical" ? "critical" : st.to === "severe" ? "warning" : "info",
    });
  }

  // Add harm events
  for (const harm of harmEvents) {
    replayEvents.push({
      tick: harm.tick,
      simSeconds: harm.simSeconds,
      kind: "harm_event",
      label: harm.description.slice(0, 60),
      detail: harm.description,
      severity: ["severe", "preventable_arrest"].includes(harm.level) ? "critical" : "warning",
    });
  }

  // Sort all events chronologically
  replayEvents.sort((a, b) => a.tick - b.tick || a.kind.localeCompare(b.kind));

  // ── Missed opportunities ───────────────────────────────────────────────────
  const missedOpportunities: MissedOpportunity[] = detectMissedOpportunities(
    snapshots, scoringEvents, stageTransitions,
  );

  // ── Optimal path comparisons ───────────────────────────────────────────────
  const optimalPathComparisons = buildOptimalPathComparisons(
    snapshots, scoringEvents, stageTransitions,
  );

  // ── Pre-computed per-tick arrays for scrubber ─────────────────────────────
  const alarmCountByTick: number[] = snapshots.map((s) => s.state.ecgRhythmKey
    ? (["ventricular_fibrillation", "asystole", "pea"].includes(s.state.ecgRhythmKey) ? 3
      : s.state.heartRate > 150 || s.state.spo2 < 85 || s.state.systolicBP < 70 ? 2
      : s.state.heartRate > 100 || s.state.spo2 < 92 || s.state.systolicBP < 90 ? 1
      : 0)
    : 0,
  );

  const severityByTick: Array<0 | 1 | 2> = snapshots.map((s) => {
    const stage = s.state.conditionStage;
    if (stage === "critical" || stage === "severe") return 2;
    if (stage === "developing") return 1;
    return 0;
  });

  // ── Auto-bookmarks ─────────────────────────────────────────────────────────
  const bookmarks: ReplayBookmark[] = [
    { tick: 0, simSeconds: 0, label: "Session start", kind: "stage_change", autoGenerated: true },
    ...stageTransitions.map((st) => ({
      tick: st.tick,
      simSeconds: st.simSeconds,
      label: `→ ${st.to}`,
      kind: "stage_change" as const,
      autoGenerated: true,
    })),
    ...harmEvents.map((h) => ({
      tick: h.tick,
      simSeconds: h.simSeconds,
      label: `Harm: ${h.level}`,
      kind: "harm" as const,
      autoGenerated: true,
    })),
    ...scoringEvents
      .filter((e) => e.type === "intervention")
      .slice(0, 5)
      .map((e) => ({
        tick: e.tick,
        simSeconds: e.simSeconds,
        label: e.detail.slice(0, 40),
        kind: "intervention" as const,
        autoGenerated: true,
      })),
    ...missedOpportunities.filter((m) => m.isHarm).map((m) => ({
      tick: m.tick,
      simSeconds: m.simSeconds,
      label: `Missed: ${m.description.slice(0, 35)}`,
      kind: "missed" as const,
      autoGenerated: true,
    })),
  ].sort((a, b) => a.tick - b.tick);

  replayEvents.push({
    tick: totalTicks,
    simSeconds: totalSimSeconds,
    kind: "session_end",
    label: "Session ended",
    detail: `Final stage: ${snapshots[snapshots.length - 1]?.state.conditionStage ?? "unknown"}`,
    severity: "info",
  });

  return {
    sessionId,
    conditionKey,
    snapshots,
    events: replayEvents,
    stageTransitions,
    missedOpportunities,
    optimalPathComparisons,
    bookmarks,
    totalTicks,
    totalSimSeconds,
    alarmCountByTick,
    severityByTick,
  };
}

// ─── Seek engine ──────────────────────────────────────────────────────────────

/**
 * Seek to a specific tick and return a ReplayCursor for the display layer.
 */
export function seekToTick(
  timeline: ReplayTimeline,
  targetTick: number,
): ReplayCursor | null {
  const clampedTick = Math.max(0, Math.min(targetTick, timeline.totalTicks));
  const snapshot = timeline.snapshots.find((s) => s.tick >= clampedTick)
    ?? timeline.snapshots[timeline.snapshots.length - 1];
  if (!snapshot) return null;

  const eventsAtTick = timeline.events.filter((e) => e.tick === snapshot.tick);
  const activeInterventions = snapshot.state.activeInterventions.map((ai) => ai.key);
  const alarmLabels = deriveAlarmLabels(snapshot.state);
  const educationalNote = buildEducationalNote(snapshot, timeline, eventsAtTick);

  return {
    tick: snapshot.tick,
    simSeconds: snapshot.simSeconds,
    snapshot,
    eventsAtTick,
    activeInterventions,
    conditionStage: snapshot.state.conditionStage,
    alarmLabels,
    educationalNote,
  };
}

/**
 * Seek to the nearest critical moment of a given kind.
 */
export function seekToEvent(
  timeline: ReplayTimeline,
  kind: ReplayEventKind,
  fromTick = 0,
): ReplayCursor | null {
  const event = timeline.events.find((e) => e.kind === kind && e.tick >= fromTick);
  if (!event) return null;
  return seekToTick(timeline, event.tick);
}

/**
 * Seek to the next bookmark after the current tick.
 */
export function seekToNextBookmark(
  timeline: ReplayTimeline,
  currentTick: number,
): ReplayCursor | null {
  const bm = timeline.bookmarks.find((b) => b.tick > currentTick);
  if (!bm) return null;
  return seekToTick(timeline, bm.tick);
}

// ─── Export / storage helpers ─────────────────────────────────────────────────

/**
 * Compact a ReplayTimeline for storage — strip large snapshot arrays,
 * retain events, bookmarks, and stage transitions.
 * The full snapshots array can be re-fetched from the session report.
 */
export function compactReplayTimeline(
  timeline: ReplayTimeline,
): Omit<ReplayTimeline, "snapshots" | "alarmCountByTick" | "severityByTick"> {
  const { snapshots: _s, alarmCountByTick: _a, severityByTick: _sv, ...rest } = timeline;
  return rest;
}

/**
 * Build a plain-text timeline summary for Stripe dispute evidence / institutional review.
 */
export function buildReplayTextSummary(timeline: ReplayTimeline): string {
  const lines: string[] = [
    `SESSION REPLAY SUMMARY`,
    `Condition: ${timeline.conditionKey}`,
    `Duration: ${Math.round(timeline.totalSimSeconds / 60)} sim-minutes (${timeline.totalTicks} ticks)`,
    ``,
    `STAGE PROGRESSION`,
    ...timeline.stageTransitions.map(
      (st) => `  t+${Math.round(st.simSeconds / 60)}min: ${st.from} → ${st.to}${st.hadIntervened ? " (treated)" : " (untreated)"}`,
    ),
    ``,
    `LEARNER ACTIONS`,
    ...timeline.events
      .filter((e) => ["intervention", "recognition", "escalation"].includes(e.kind))
      .map((e) => `  t+${Math.round(e.simSeconds / 60)}min: [${e.kind.toUpperCase()}] ${e.label}`),
    ``,
    `MISSED OPPORTUNITIES`,
    ...timeline.missedOpportunities.map(
      (m) => `  t+${Math.round(m.simSeconds / 60)}min: ${m.description} — Expected: ${m.expectedAction}${m.isHarm ? " [HARM]" : ""}`,
    ),
  ];
  return lines.join("\n");
}

// ─── Internal helpers ─────────────────────────────────────────────────────────

function emptyTimeline(sessionId: string, conditionKey: string): ReplayTimeline {
  return {
    sessionId, conditionKey, snapshots: [], events: [], stageTransitions: [],
    missedOpportunities: [], optimalPathComparisons: [], bookmarks: [],
    totalTicks: 0, totalSimSeconds: 0, alarmCountByTick: [], severityByTick: [],
  };
}

function detectKeyVital(prev: Readonly<PhysiologyState>, curr: Readonly<PhysiologyState>): string {
  const deltas: [string, number][] = [
    ["HR", Math.abs(curr.heartRate - prev.heartRate)],
    ["SBP", Math.abs(curr.systolicBP - prev.systolicBP)],
    ["SpO₂", Math.abs(curr.spo2 - prev.spo2)],
    ["GCS", Math.abs(curr.gcs - prev.gcs)],
    ["MAP", Math.abs(curr.map - prev.map)],
  ];
  deltas.sort((a, b) => b[1] - a[1]);
  return deltas[0]?.[0] ?? "vitals";
}

function scoringEventQuality(
  event: ScoringEvent,
  stageTransitions: StageTransition[],
): ReplayEvent["quality"] {
  const criticalTransition = stageTransitions.find((st) => st.to === "critical");
  if (!criticalTransition) return "optimal";
  if (event.tick <= criticalTransition.tick - 4) return "optimal";
  if (event.tick <= criticalTransition.tick) return "delayed";
  return "missed";
}

function detectMissedOpportunities(
  snapshots: PhysiologySnapshot[],
  scoringEvents: ScoringEvent[],
  stageTransitions: StageTransition[],
): MissedOpportunity[] {
  const missed: MissedOpportunity[] = [];

  // Check: did learner intervene before severe stage?
  const severeTransition = stageTransitions.find((st) => st.to === "severe");
  if (severeTransition && !severeTransition.hadIntervened) {
    const interventionAfter = scoringEvents.find(
      (e) => e.type === "intervention" && e.tick > severeTransition.tick,
    );
    missed.push({
      tick: severeTransition.tick,
      simSeconds: severeTransition.simSeconds,
      description: "Patient deteriorated to severe stage before any intervention",
      expectedAction: "Apply at least one indicated intervention during the developing stage",
      delayTicks: interventionAfter ? interventionAfter.tick - severeTransition.tick : null,
      isHarm: false,
    });
  }

  // Check: did learner escalate (recognition event) before critical stage?
  const criticalTransition = stageTransitions.find((st) => st.to === "critical");
  if (criticalTransition) {
    const escalated = scoringEvents.some(
      (e) => e.type === "escalation" && e.tick < criticalTransition.tick,
    );
    if (!escalated) {
      missed.push({
        tick: criticalTransition.tick,
        simSeconds: criticalTransition.simSeconds,
        description: "No escalation event recorded before critical stage",
        expectedAction: "Escalate to provider or call rapid response before critical deterioration",
        delayTicks: null,
        isHarm: true,
      });
    }
  }

  // Check: SpO₂ < 88 for more than 3 ticks without O₂ applied
  let hypoxiaTicks = 0;
  let o2Applied = false;
  for (const snap of snapshots) {
    if (scoringEvents.some((e) => e.detail.toLowerCase().includes("oxygen") && e.tick <= snap.tick)) {
      o2Applied = true;
    }
    if (snap.state.spo2 < 88 && !o2Applied) {
      hypoxiaTicks++;
      if (hypoxiaTicks === 3) {
        missed.push({
          tick: snap.tick,
          simSeconds: snap.simSeconds,
          description: `SpO₂ remained below 88% for ${hypoxiaTicks} ticks without supplemental oxygen`,
          expectedAction: "Apply supplemental oxygen when SpO₂ drops below 90%",
          delayTicks: null,
          isHarm: true,
        });
      }
    } else {
      hypoxiaTicks = 0;
    }
  }

  return missed;
}

function buildOptimalPathComparisons(
  snapshots: PhysiologySnapshot[],
  scoringEvents: ScoringEvent[],
  stageTransitions: StageTransition[],
): OptimalPathComparison[] {
  const comparisons: OptimalPathComparison[] = [];

  // Compare at each stage transition: was a response given within optimal window?
  for (const st of stageTransitions) {
    const optimalTick = st.tick + 2; // Optimal: respond within 2 ticks of stage change
    const learnerEvent = scoringEvents.find(
      (e) => e.type === "intervention" && e.tick >= st.tick && e.tick <= optimalTick + 4,
    );

    comparisons.push({
      tick: st.tick,
      simSeconds: st.simSeconds,
      optimalAction: `Respond to stage change to "${st.to}" within 1–2 ticks`,
      learnerAction: learnerEvent?.detail ?? null,
      timingDeltaTicks: learnerEvent ? learnerEvent.tick - optimalTick : null,
      wasCorrect: !!learnerEvent,
      coachingNote: learnerEvent
        ? learnerEvent.tick <= optimalTick
          ? "Excellent response timing — within optimal window."
          : `Response delayed ${learnerEvent.tick - optimalTick} ticks past optimal window.`
        : `No intervention recorded after "${st.from} → ${st.to}" transition.`,
    });
  }

  return comparisons;
}

function deriveAlarmLabels(state: Readonly<PhysiologyState>): string[] {
  const alarms: string[] = [];
  if (state.heartRate > 150) alarms.push(`HR ${Math.round(state.heartRate)} — Tachycardia`);
  else if (state.heartRate < 40) alarms.push(`HR ${Math.round(state.heartRate)} — Bradycardia`);
  if (state.systolicBP < 90) alarms.push(`SBP ${Math.round(state.systolicBP)} — Hypotension`);
  if (state.spo2 < 90) alarms.push(`SpO₂ ${Math.round(state.spo2)}% — Hypoxemia`);
  if (["ventricular_fibrillation", "asystole"].includes(state.ecgRhythmKey)) {
    alarms.push(`ARREST RHYTHM — ${state.ecgRhythmKey.replace(/_/g, " ").toUpperCase()}`);
  }
  return alarms;
}

function buildEducationalNote(
  snapshot: PhysiologySnapshot,
  timeline: ReplayTimeline,
  eventsAtTick: ReplayEvent[],
): string | null {
  const stageChange = eventsAtTick.find((e) => e.kind === "stage_transition");
  if (stageChange) return stageChange.detail;

  const harm = eventsAtTick.find((e) => e.kind === "harm_event");
  if (harm) return harm.detail;

  const missed = timeline.missedOpportunities.find((m) => m.tick === snapshot.tick);
  if (missed) return `Missed opportunity: ${missed.expectedAction}`;

  return null;
}
