import type { MonitorMode } from "./physiology-state";

// ─── Profession ───────────────────────────────────────────────────────────────

export type SimulationProfession = "RN" | "RPN" | "NP" | "RT" | "NEW_GRAD";

// ─── Difficulty ───────────────────────────────────────────────────────────────

export type SimulationDifficulty = "foundational" | "developing" | "proficient" | "advanced";

// ─── NGN format ───────────────────────────────────────────────────────────────

export type NgnQuestionFormat = "bowtie" | "matrix" | "sata" | "prioritization" | "cloze";

// ─── Simulation definition ────────────────────────────────────────────────────

export interface SimulationDefinition {
  id: string;
  title: string;
  profession: SimulationProfession[];
  specialty: string[];
  conditionKey: string;
  pathwayId: string;
  difficulty: SimulationDifficulty;
  /** Estimated total session time in minutes. */
  estimatedMinutes: number;
  /** Monitor mode for this simulation. */
  monitorMode: MonitorMode;
  /** Stage the condition opens at (default "early"). */
  openingStage?: "early" | "developing" | "severe";
  /** Whether to show overlay by default. */
  defaultOverlay: boolean;
  /** Whether interventions are available (some assessment-only sims hide them). */
  showInterventions: boolean;
  patientBrief: string;
  sbar: { situation: string; background: string; assessment: string; recommendation: string };
  learningObjectives: string[];
  criticalActions: CriticalAction[];
  documentationPrompts: string[];
  ngnFormats: NgnQuestionFormat[];
  /** Primary NCJMM domains exercised by this simulation. */
  primaryNcjmmDomains: string[];
  /** Clinical tags for filtering and search. */
  tags: string[];
}

export interface CriticalAction {
  /** Ticks from session start within which this action must occur. */
  timeLimitTicks: number;
  description: string;
  /** Intervention key, or null if it is a recognition/escalation action. */
  interventionKey: string | null;
  /** Is failure a patient-safety event? */
  isHarmIfMissed: boolean;
}

