export function formatAdminEditPublishMarkdownTable(rows: readonly AdminEditPublishSurfaceRow[]): string {
  const header = [
    "contentType",
    "adminEditRoute",
    "storageSource",
    "publicRoute",
    "canPreview",
    "canPublish",
    "revalidatesPublicRoute",
    "revalidatesSitemap",
    "status",
    "notes",
  ];
  const lines = [header.join(" | "), header.map(() => "---").join(" | ")];
  for (const r of rows) {
    lines.push(
      [
        r.contentType,
        r.adminEditRoute.replace(/\|/g, "\\|"),
        r.storageSource.replace(/\|/g, "\\|"),
        r.publicRoute.replace(/\|/g, "\\|"),
        String(r.canPreview),
        String(r.canPublish),
        String(r.revalidatesPublicRoute),
        String(r.revalidatesSitemap),
        r.status,
        r.notes.replace(/\|/g, "\\|").slice(0, 200),
      ].join(" | "),
    );
  }
  return lines.join("\n");
}
