const v = (HR: string, BP: string, RR: string, SpO2: string, T: string): Prisma.InputJsonValue => ({
  HR,
  BP,
  RR,
  SpO2,
  T,
});

function wDelayChart(ctx: string) {
  return optWrongDelay(
    "Complete lengthy bedside charting before any new monitoring or orders.",
    `Documentation should not delay time-sensitive stabilization in ${ctx}.`,
  );
}

function wLimitIgnoreVitals(ctx: string) {
  return optWrongLimit(
    "Tell the patient to rest and you will return later without reassessing vitals.",
    `Misses evolving instability; repeat assessment and escalation are required in ${ctx}.`,
  );
}

function wDelaySocial(ctx: string) {
  return optWrongDelay(
    "Spend 20 minutes on an extended social history before focused assessment.",
    `Social history can wait once red flags for ${ctx} are ruled in or out.`,
  );
}

type StagePack = {
  lead: string;
  stem: string;
  af: string;
  vit: Prisma.InputJsonValue;
  lu: Prisma.InputJsonValue | null;
  correct: string;
  optRat: string;
  stageRat: string;
  /** Optional fourth distractor (dangerous clinical error). */
  badLimit?: { text: string; rationale: string };
};

function compactFive(
  slug: string,
  categoryId: CanonicalStudyCategoryId,
  titleBase: string,
  age: string,
  concern: string,
  hx: string,
  meds: string | null,
  iv: Prisma.InputJsonValue,
  af0: string,
  labs: Prisma.InputJsonValue | null,
  stages: [StagePack, StagePack, StagePack, StagePack, StagePack],
): RawTopicDef {
  return {
    slug,
    categoryId,
    titleBase,
    patientAgeContext: age,
    presentingConcern: concern,
    briefHistory: hx,
    medicationsAllergies: meds,
    initialVitals: iv,
    assessmentFindings: af0,
    labsDiagnostics: labs,
    stages: stages.map((s) => ({
      scenarioLead: s.lead,
      stemTail: s.stem,
      assessmentFindings: s.af,
      vitals: s.vit,
      labUpdates: s.lu,
      rationale: s.stageRat,
      options: [
        optCorrect(s.correct, s.optRat),
        wLimitIgnoreVitals(slug),
        wDelayChart(slug),
        s.badLimit ?? wDelaySocial(slug),
      ],
    })),
  };
}

