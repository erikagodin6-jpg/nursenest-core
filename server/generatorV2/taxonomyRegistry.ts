export function getAllCanonicalTopics(): string[] {
  const topics: string[] = [];
  for (const system of VALID_BODY_SYSTEMS) {
    topics.push(...TOPICS_BY_SYSTEM[system]);
  }
  return topics;
}

export function getCanonicalSystemForTopic(topic: string): ValidBodySystem | null {
  for (const system of VALID_BODY_SYSTEMS) {
    if (TOPICS_BY_SYSTEM[system].some(t => t.toLowerCase() === topic.toLowerCase())) {
      return system;
    }
  }
  return null;
}

export function isValidSystem(system: string): boolean {
  return VALID_BODY_SYSTEMS.includes(system as ValidBodySystem);
}

export function isValidTopic(topic: string, system?: string): boolean {
  if (system) {
    const validSystem = system as ValidBodySystem;
    if (!TOPICS_BY_SYSTEM[validSystem]) return false;
    return TOPICS_BY_SYSTEM[validSystem].some(t => t.toLowerCase() === topic.toLowerCase());
  }
  return getCanonicalSystemForTopic(topic) !== null;
}

export function getGeneralTopic(system: ValidBodySystem): string {
  const generalTopic = TOPICS_BY_SYSTEM[system]?.find(t => t.endsWith("General"));
  return generalTopic || `${system} General`;
}
