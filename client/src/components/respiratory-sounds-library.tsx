function generateSound(ctx: AudioContext, type: BreathSound["waveformType"], duration: number, gainNode: GainNode): void {
  const { t } = useI18n();
  const now = ctx.currentTime;
  const end = now + duration;

  switch (type) {
    case "vesicular": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const phase = (t % 2) / 2;
        const envelope = phase < 0.75 ? Math.sin(phase * Math.PI / 0.75) * 0.6 : Math.sin((phase - 0.75) * Math.PI / 0.25) * 0.2;
        data[i] = (Math.random() * 2 - 1) * envelope * 0.15;
      }
      noise.buffer = buf;
      const lp = ctx.createBiquadFilter();
      lp.type = "lowpass";
      lp.frequency.value = 400;
      noise.connect(lp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    case "bronchovesicular": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const phase = (t % 2) / 2;
        const envelope = phase < 0.5 ? Math.sin(phase * Math.PI / 0.5) * 0.5 : Math.sin((phase - 0.5) * Math.PI / 0.5) * 0.5;
        data[i] = (Math.random() * 2 - 1) * envelope * 0.2;
      }
      noise.buffer = buf;
      const bp = ctx.createBiquadFilter();
      bp.type = "bandpass";
      bp.frequency.value = 500;
      bp.Q.value = 1;
      noise.connect(bp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    case "bronchial": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const phase = (t % 2) / 2;
        const envelope = phase < 0.35 ? Math.sin(phase * Math.PI / 0.35) * 0.4 : phase < 0.4 ? 0 : Math.sin((phase - 0.4) * Math.PI / 0.6) * 0.7;
        data[i] = (Math.random() * 2 - 1) * envelope * 0.25;
      }
      noise.buffer = buf;
      const hp = ctx.createBiquadFilter();
      hp.type = "highpass";
      hp.frequency.value = 300;
      noise.connect(hp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    case "crackles-fine": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const cyclePos = (t % 2) / 2;
        let val = 0;
        if (cyclePos > 0.5 && cyclePos < 0.9) {
          if (Math.random() < 0.008) {
            const clickLen = Math.min(Math.floor(Math.random() * 60 + 20), data.length - i);
            for (let j = 0; j < clickLen && (i + j) < data.length; j++) {
              data[i + j] = (Math.random() * 2 - 1) * Math.exp(-j / 15) * 0.6;
            }
          }
        }
        val += (Math.random() * 2 - 1) * 0.02;
        data[i] = val;
      }
      noise.buffer = buf;
      const hp = ctx.createBiquadFilter();
      hp.type = "highpass";
      hp.frequency.value = 600;
      noise.connect(hp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    case "crackles-coarse": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const cyclePos = (t % 2) / 2;
        let val = 0;
        if (cyclePos > 0.1 && cyclePos < 0.8) {
          if (Math.random() < 0.005) {
            const clickLen = Math.min(Math.floor(Math.random() * 200 + 80), data.length - i);
            for (let j = 0; j < clickLen && (i + j) < data.length; j++) {
              data[i + j] = (Math.random() * 2 - 1) * Math.exp(-j / 60) * 0.8;
            }
          }
        }
        val += (Math.random() * 2 - 1) * 0.03;
        data[i] = val;
      }
      noise.buffer = buf;
      const lp = ctx.createBiquadFilter();
      lp.type = "lowpass";
      lp.frequency.value = 500;
      noise.connect(lp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    case "wheeze": {
      const osc = ctx.createOscillator();
      osc.type = "sine";
      osc.frequency.value = 400;
      const lfo = ctx.createOscillator();
      lfo.type = "sine";
      lfo.frequency.value = 3;
      const lfoGain = ctx.createGain();
      lfoGain.gain.value = 50;
      lfo.connect(lfoGain).connect(osc.frequency);
      const modGain = ctx.createGain();
      modGain.gain.value = 0;
      const cycleLen = 2;
      const cycles = Math.ceil(duration / cycleLen);
      for (let c = 0; c < cycles; c++) {
        const cStart = now + c * cycleLen;
        modGain.gain.setValueAtTime(0.02, cStart);
        modGain.gain.linearRampToValueAtTime(0.02, cStart + 0.3);
        modGain.gain.linearRampToValueAtTime(0.25, cStart + 0.5);
        modGain.gain.linearRampToValueAtTime(0.3, cStart + 1.4);
        modGain.gain.linearRampToValueAtTime(0.05, cStart + 1.8);
        modGain.gain.linearRampToValueAtTime(0.02, cStart + 2.0);
      }
      osc.connect(modGain).connect(gainNode);
      osc.start(now);
      osc.stop(end);
      lfo.start(now);
      lfo.stop(end);
      break;
    }
    case "rhonchi": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const base = Math.sin(2 * Math.PI * 80 * t) * 0.3 + Math.sin(2 * Math.PI * 120 * t) * 0.2;
        const envelope = Math.sin((t % 2) * Math.PI / 2) * 0.7;
        data[i] = (base + (Math.random() * 2 - 1) * 0.15) * envelope;
      }
      noise.buffer = buf;
      const lp = ctx.createBiquadFilter();
      lp.type = "lowpass";
      lp.frequency.value = 300;
      noise.connect(lp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    case "stridor": {
      const osc = ctx.createOscillator();
      osc.type = "sawtooth";
      osc.frequency.value = 700;
      const modGain = ctx.createGain();
      modGain.gain.value = 0;
      const cycleLen = 2;
      const cycles = Math.ceil(duration / cycleLen);
      for (let c = 0; c < cycles; c++) {
        const cStart = now + c * cycleLen;
        modGain.gain.setValueAtTime(0.02, cStart);
        modGain.gain.linearRampToValueAtTime(0.3, cStart + 0.3);
        modGain.gain.linearRampToValueAtTime(0.25, cStart + 0.8);
        modGain.gain.linearRampToValueAtTime(0.02, cStart + 1.0);
        modGain.gain.setValueAtTime(0.02, cStart + 1.0);
        modGain.gain.linearRampToValueAtTime(0.08, cStart + 1.3);
        modGain.gain.linearRampToValueAtTime(0.02, cStart + 1.8);
      }
      const lp = ctx.createBiquadFilter();
      lp.type = "lowpass";
      lp.frequency.value = 1200;
      osc.connect(modGain).connect(lp).connect(gainNode);
      osc.start(now);
      osc.stop(end);
      break;
    }
    case "friction-rub": {
      const noise = ctx.createBufferSource();
      const buf = ctx.createBuffer(1, ctx.sampleRate * duration, ctx.sampleRate);
      const data = buf.getChannelData(0);
      for (let i = 0; i < data.length; i++) {
        const t = i / ctx.sampleRate;
        const cyclePos = (t % 2) / 2;
        let envelope = 0;
        if (cyclePos < 0.4) {
          const scrub = Math.sin(cyclePos * Math.PI / 0.4);
          envelope = scrub * (0.5 + 0.5 * Math.sin(2 * Math.PI * 30 * t));
        } else if (cyclePos > 0.5 && cyclePos < 0.9) {
          const adj = (cyclePos - 0.5) / 0.4;
          const scrub = Math.sin(adj * Math.PI);
          envelope = scrub * (0.5 + 0.5 * Math.sin(2 * Math.PI * 25 * t));
        }
        data[i] = (Math.random() * 2 - 1) * envelope * 0.35;
      }
      noise.buffer = buf;
      const bp = ctx.createBiquadFilter();
      bp.type = "bandpass";
      bp.frequency.value = 350;
      bp.Q.value = 2;
      noise.connect(bp).connect(gainNode);
      noise.start(now);
      noise.stop(end);
      break;
    }
    default:
      break;
  }
}

function SoundCard({ sound }: { sound: BreathSound }) {
  const audioElRef = useRef<HTMLAudioElement | null>(null);
  const ctxRef = useRef<AudioContext | null>(null);
  const gainRef = useRef<GainNode | null>(null);
  const [isPlaying, setIsPlaying] = useState(false);
  const [volume, setVolume] = useState(0.7);
  const [isLooping, setIsLooping] = useState(false);
  const loopRef = useRef(false);
  const playingRef = useRef(false);
  const timerRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  const usesAudioFile = !!sound.audioSrc;

  useEffect(() => {
    loopRef.current = isLooping;
    if (audioElRef.current) {
      audioElRef.current.loop = isLooping;
    }
  }, [isLooping]);

  const stopPlayback = useCallback(() => {
    playingRef.current = false;
    setIsPlaying(false);
    if (audioElRef.current) {
      audioElRef.current.pause();
      audioElRef.current.currentTime = 0;
    }
    if (timerRef.current) {
      clearTimeout(timerRef.current);
      timerRef.current = null;
    }
    if (ctxRef.current && ctxRef.current.state !== "closed") {
      ctxRef.current.close().catch(() => {});
      ctxRef.current = null;
    }
  }, []);

  const startPlayback = useCallback(() => {
    if (playingRef.current) {
      stopPlayback();
      return;
    }

    if (usesAudioFile) {
      if (!audioElRef.current) {
        audioElRef.current = new Audio(sound.audioSrc);
      }
      audioElRef.current.loop = loopRef.current;
      audioElRef.current.volume = volume;
      audioElRef.current.play().catch(() => {});
      playingRef.current = true;
      setIsPlaying(true);
      if (!loopRef.current) {
        audioElRef.current.onended = () => {
          playingRef.current = false;
          setIsPlaying(false);
        };
      }
      return;
    }

    const ctx = new AudioContext();
    ctxRef.current = ctx;
    const gain = ctx.createGain();
    gain.gain.value = volume;
    gain.connect(ctx.destination);
    gainRef.current = gain;

    const dur = 6;
    playingRef.current = true;
    setIsPlaying(true);

    function playCycle() {
      if (!playingRef.current || !ctxRef.current || ctxRef.current.state === "closed") return;
      generateSound(ctxRef.current, sound.waveformType, dur, gainRef.current!);

      timerRef.current = setTimeout(() => {
        if (loopRef.current && playingRef.current) {
          playCycle();
        } else {
          stopPlayback();
        }
      }, dur * 1000);
    }

    playCycle();
  }, [sound.waveformType, sound.audioSrc, volume, stopPlayback, usesAudioFile]);

  useEffect(() => {
    return () => {
      playingRef.current = false;
      if (audioElRef.current) {
        audioElRef.current.pause();
        audioElRef.current = null;
      }
      if (timerRef.current) clearTimeout(timerRef.current);
      if (ctxRef.current && ctxRef.current.state !== "closed") {
        ctxRef.current.close().catch(() => {});
      }
    };
  }, []);

  const handleVolumeChange = useCallback((val: number[]) => {
    setVolume(val[0]);
    if (audioElRef.current) {
      audioElRef.current.volume = val[0];
    }
    if (gainRef.current) gainRef.current.gain.value = val[0];
  }, []);

  const categoryColors = {
    normal: { bg: "bg-green-50", border: "border-green-200", badge: "bg-green-100 text-green-700" },
    adventitious: { bg: "bg-red-50", border: "border-red-200", badge: "bg-red-100 text-red-700" },
    voice: { bg: "bg-purple-50", border: "border-purple-200", badge: "bg-purple-100 text-purple-700" },
  };

  const colors = categoryColors[sound.category];

  return (
    <div className={`rounded-xl p-4 ${colors.bg} ${colors.border} border shadow-sm`} data-testid={`sound-card-${sound.id}`}>
      <div className="flex items-start justify-between mb-2">
        <div>
          <h4 className="font-bold text-gray-900 text-sm">{sound.name}</h4>
          <span className={`inline-block text-[10px] font-semibold px-2 py-0.5 rounded-full mt-1 ${colors.badge}`}>
            {sound.category === "normal" ? "Normal" : sound.category === "adventitious" ? "Adventitious" : "Voice Sound"}
          </span>
        </div>
        <Wind className="w-5 h-5 text-gray-400 flex-shrink-0 mt-0.5" />
      </div>

      <p className="text-xs text-gray-600 mb-3 leading-relaxed">{sound.description}</p>

      <div className="grid grid-cols-2 gap-2 text-xs mb-3">
        <div>
          <span className="font-semibold text-gray-700">{t("components.respiratorySoundsLibrary.timing")}</span>
          <p className="text-gray-600">{sound.timing}</p>
        </div>
        <div>
          <span className="font-semibold text-gray-700">{t("components.respiratorySoundsLibrary.pitch")}</span>
          <p className="text-gray-600">{sound.pitch}</p>
        </div>
      </div>

      <div className="mb-3">
        <span className="text-xs font-semibold text-gray-700">{t("components.respiratorySoundsLibrary.clinicalSignificance")}</span>
        <p className="text-xs text-gray-600">{sound.clinicalSignificance}</p>
      </div>

      <div className="mb-3">
        <span className="text-xs font-semibold text-gray-700">{t("components.respiratorySoundsLibrary.commonCauses")}</span>
        <ul className="mt-1 space-y-0.5">
          {sound.commonCauses.map((cause, i) => (
            <li key={i} className="text-xs text-gray-600">- {cause}</li>
          ))}
        </ul>
      </div>

      <div className="flex items-center gap-2 pt-2 border-t border-gray-200/60">
        <Button
          variant="ghost"
          size="icon"
          className={`h-9 w-9 rounded-full ${isPlaying ? "bg-primary text-white" : ""}`}
          onClick={startPlayback}
          data-testid={`button-play-sound-${sound.id}`}
        >
          {isPlaying ? <Pause className="w-4 h-4" /> : <Play className="w-4 h-4" />}
        </Button>
        <span className="text-[10px] text-gray-400 w-16">{isPlaying ? "Playing..." : usesAudioFile ? "Recording" : "Synthesized"}</span>
        <Button
          variant="ghost"
          size="icon"
          className={`h-7 w-7 ${isLooping ? "text-primary bg-primary/10" : "text-gray-400"}`}
          onClick={() => setIsLooping(!isLooping)}
          data-testid={`button-loop-sound-${sound.id}`}
        >
          <Repeat className="w-3.5 h-3.5" />
        </Button>
        <div className="flex items-center gap-1 ml-auto">
          {volume === 0 ? <VolumeX className="w-3.5 h-3.5 text-gray-400" /> : <Volume2 className="w-3.5 h-3.5 text-gray-400" />}
          <Slider
            value={[volume]} min={0} max={1} step={0.05}
            onValueChange={handleVolumeChange}
            className="w-16"
            data-testid={`slider-volume-sound-${sound.id}`}
          />
        </div>
      </div>
    </div>
  );
}

